import { url } from './usersData';

export const createSub = (id, payload) => new Promise((resolve, reject) => {
  fetch(`${url}/subscriptions/${id}`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

export const getSubs = () => new Promise((resolve, reject) => {
  fetch(`${url}/subscriptions`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});

